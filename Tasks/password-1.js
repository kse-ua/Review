const generatePassword = (lengthOfPassword) => {
  const max = characters.length;
  const key = [];
  key.length = lengthOfPassword;
  for (let i = 0; i < lengthOfPassword; i++) {
    const Index = Math.floor(Math.random() * max);
    key[i] = characters[Index];
  }
  return key;
};

